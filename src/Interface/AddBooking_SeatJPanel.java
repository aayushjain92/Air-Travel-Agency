    private BufferedImage resize(BufferedImage originalImage, int newW, int newH) {
        BufferedImage resizedImage = new BufferedImage(newW, newH, BufferedImage.TYPE_INT_ARGB);

        Graphics2D g2d = resizedImage.createGraphics();
        g2d.drawImage(originalImage, 0, 0, newW, newH, null);
        g2d.dispose();

        return resizedImage;
    }

    public void createseats() {
        for (String a : seatlocations) {
            for (int i = 1; i <= 25; i++) {
                seats.add(a + i);
            }
        }

        Iterator itr = seats.iterator();
        while (itr.hasNext()) {
            String x = (String) itr.next();
            booking = bookingDir.searchBookingbyFlightSeat(fleet.getFleetId(), x);
            if (booking != null) {
                itr.remove();
            }
        }

    }

    public void populateseats() {
        cbSeatNumber.removeAllItems();
        if ("Window".equals(cbSeatLocation.getSelectedItem().toString())) {
            for (String a : seats) {
                if (a.contains("A") || a.contains("F")) {
                    cbSeatNumber.addItem(a);
                }
            }
        } else if ("Middle".equals(cbSeatLocation.getSelectedItem().toString())) {
            for (String a : seats) {
                if (a.contains("B") || a.contains("E")) {
                    cbSeatNumber.addItem(a);
                }
            }
        } else {
            for (String a : seats) {
                if (a.contains("C") || a.contains("D")) {
                    cbSeatNumber.addItem(a);
                }
            }
        }
    }

