    private void btnBookFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookFlightActionPerformed
        // TODO add your handling code here:
        if ("".equals(cbSeatNumber.getSelectedItem().toString())) {
            JOptionPane.showMessageDialog(null, "Please fill all the required fields.");
            return;
        }
        Booking booking = bookingDir.addBooking();
        booking.setCustomerEmail(customer.getEmail());
        booking.setAirlineName(fleet.getAirline());
        booking.setAircraftName(fleet.getFleetId());
        booking.setSeat(cbSeatNumber.getSelectedItem().toString());
        fleet.setSeatLeft(fleet.getSeatLeft() - 1);
        JOptionPane.showMessageDialog(null, "Flight Added Successfully");
        rightPanel.removeAll();
    }//GEN-LAST:event_btnBookFlightActionPerformed

